console.log("6. ", cars >= motorcycles); //false
console.log("7. ", cars <= motorcycles); //true
console.log("8. ", cars + motorcycles != buses); //false
console.log("9. ", buses - motorcycles == cars); //true
console.log("10. ", cars * buses == motorcycles); //false
